/**
 * Toán tử gán
 * 
 * Toán tử     Ví dụ        Tương đương
 * =           x = y        x = y
 * +=          x += y       x = x + y
 * -=          x -= y       x = x - y
 * *=          x *= y       x = x * y
 * /=          x /= y       x = x / y
 * **=         x **= y      x = x ** y
 * 
 */

console.log("Toán tử gán: ");

var a = 1;

a += 2;

console.log("a: " + a);